package com.lab111.labwork8;

/**
 * The 'Expression' class for holding algebraic expressions
 * @author Diachenlo Tetiana
 */
public class Expression {
    /**
     * Field for holding this algebraic expression
     */
    public String expr;

    /**
     * Constructor for the expression
     * @param ex - expression to be saved in the instance of this class
     */
    public Expression(String ex) {
        this.expr = ex;
    }
}
